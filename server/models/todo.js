// plain library
var mongoose = require('mongoose');



// validator
var Todo = mongoose.model('Todo', {
  text: {
    type: String,
    required: true,
    minlength: 1,
    trim: true
  },
  completed: {
    type: Boolean,
    default: false
  },
  completedAt: {
    type: Number,
    default: 000
  }
});

module.exports = {
  Todo
};